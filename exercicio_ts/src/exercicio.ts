// Função de multiplicação
function multiplicar(a: number, b: number): number {
    return a * b;
}

// Função de saudação
function saudar(nome: string): string {
    return "Olá " + nome;
}

// Exemplos de uso
console.log(multiplicar(10, 3)); // Saída: 30
console.log(saudar("Priscilla")); // Saída: Olá Priscilla